package day.day03;

public class OperatorEx01 {
    public static void main(String[] args) {
        //부호 연산
        byte b = 100;
        System.out.println(b);
        //정수 타입 (byte, short, int)의 연산결과는 int타입
        int a = -b;
        System.out.println(b);

        int c = -a;
        System.out.println(c);


    }
}
